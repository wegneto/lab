    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (!txtPassword.getText().equals(txtRetryPassword.getText())){
            JOptionPane.showMessageDialog(this, "The password must be equals","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        User usr = new User();
        usr.setName(txtName.getText());
        usr.setLogin(txtLogin.getText());
        usr.setPassword(txtPassword.getText());
        
        try {
            dao.addUser(usr);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding user " + ex,"Error", JOptionPane.ERROR_MESSAGE);
        }

        setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed


