    public void loadInitialData() {
        try {
            dao = new ScheduleDAO();
            schedule = dao.getSchedules();
            tblSchedule.setModel(new MyTableModel(Schedule.class, schedule, tblSchedule));
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
