    private void loadInitialComboData() {
        try {
            cmbType.setModel(new DefaultComboBoxModel(ScheduleType.values()));
            List<Animal> animals = new AnimalDAO().getAllAnimals();
            cmbAnimal.setModel(new DefaultComboBoxModel(new Vector<Animal>(animals)));
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Errors loading data");
            return;
        }
    }
