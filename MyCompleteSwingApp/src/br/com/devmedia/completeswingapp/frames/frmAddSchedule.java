    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int result = JOptionPane.showConfirmDialog(this, "Are you sure?", "Add schedule", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (result != JOptionPane.YES_OPTION) {
            return;
        }
        
        try {
            Schedule schedule = new Schedule();
            schedule.setAnimal((Animal) cmbAnimal.getSelectedItem());
            schedule.setType((ScheduleType) cmbType.getSelectedItem());
            schedule.setDate(new java.sql.Date(Calendar.getInstance().getTimeInMillis()));
            schedule.setTotal(Float.parseFloat(txtTotal.getText()));
            dao.addSchedule(schedule);
            frame.loadInitialData();
            setVisible(false);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding schedule");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    
